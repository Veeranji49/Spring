package com.spring;

import java.util.*;

public class MapPerson {

	private Map<String, String> name;
	private Map<Integer, Integer> contact;
	private Map<Double, Double> salary;
	public Map<String, String> getName() {
		return name;
	}
	public void setName(Map<String, String> name) {
		this.name = name;
	}
	public Map<Integer, Integer> getContact() {
		return contact;
	}
	public void setContact(Map<Integer, Integer> contact) {
		this.contact = contact;
	}
	public Map<Double, Double> getSalary() {
		return salary;
	}
	public void setSalary(Map<Double, Double> salary) {
		this.salary = salary;
	}
	
	

}
